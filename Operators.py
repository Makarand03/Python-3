#Comparison Operators : Will always return a Boolean output, i.e True or False

c = 15
d = 12
e = 15
print(c>d)
print(c<d)
print(c == e)
print(c != d) #Not equal

#Logical Operators : will return Boolean output

print(c>d and c<e) #AND : True when both conditions true
print(c>d or c<e) #AND : True when any one condition true
print(not c>e) #NOT returns opposite value : if false, return True and vice-versa